//elevate zoom 

function productZoom() {

	if ($(document).width() >= 992) {
		$('.product-cover img').elevateZoom({
			zoomType: "lens",
			cursor: "pointer",
			lensShape: "round",
			lensSize: 200,
			galleryActiveClass: "active",
			zoomWindowFadeIn: 500,
			zoomWindowFadeOut: 500
		});

		$('body').on('mouseenter', '.product-cover .js-qv-product-cover', function () {
			$('.zoomContainer').remove();
			$(this).removeData('elevateZoom');
			$(this).attr('src', $(this).attr('src'));
			$(this).data('zoom-image', $(this).data('zoom-image'));
			$(this).elevateZoom({
				zoomType: "lens",
				cursor: "pointer",
				lensShape: "round",
				lensSize: 200
			});
		});

		$('body').on('click', ('.js-qv-product-images .js-thumb'), function (e) {
			e.preventDefault();
			var zoom_val = $(this).attr('src');
			/*$(".zoomLens img").attr('src', $(this).attr('data-image-large-src'));*/
			$(".zoomLens").css("background-image", "url(" + $(this).attr('data-image-large-src') + ")");
			$('.zoomWindowContainer div').css("background-image", "url(" + $(this).attr('data-image-large-src') + ")");
		});
	}

}

$(document).ready(function () {
	productZoom();
});
$(window).resize(function () {
	productZoom();
});

// Countdown
function timecounter() {
	$('.countbox.hastime').each(function(){
		var countTime = $(this).attr('data-countdown');
		//console.log(countTime);

		$(this).countdown(countTime, function(event) {
			$(this).html(
				'<div class="count-title">Hurry Up! Offer End In:</div><div class="timebox-block"><span class="timebox day"><span class="timebox-inner"><strong>'+event.strftime('%D')+'</strong> Days</span></span><span class="timebox hour"><span class="timebox-inner"><strong>'+event.strftime('%H')+'</strong> Hours</span></span><span class="timebox minute"><span class="timebox-inner"><strong>'+event.strftime('%M')+'</strong> Mins</span></span><span class="timebox second"><span class="timebox-inner"><strong>'+event.strftime('%S')+'</strong>   Secs</span></span></div>'
				// '<div class="count-title">Hurry Up! Offer End In:</div><span class="timebox day"><span class="timebox-inner"><strong>'+event.strftime('%D')+'</strong> days</span></span><span class="timebox hour"><span class="timebox-inner"><strong>'+event.strftime('%H')+'</strong> hours</span></span><span class="timebox minute"><span class="timebox-inner"><strong>'+event.strftime('%M')+'</strong> mins</span></span><span class="timebox second"><span class="timebox-inner"><strong>'+event.strftime('%S')+'</strong>   secs</span></span>'
			);
		});
		//$(this).countdown('stop');
	});
}
